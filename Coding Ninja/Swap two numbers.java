import java.util.*;
public class Solution{
    public static void swapNumber(int []a, int []b) {
        // Write your code here.
        int t=a[0];
        a[0]=b[0];
        b[0]=t;
        
    }
}

// Input: 
// 'a' = 8, 'b' = 5

// Output:
// 5 8

